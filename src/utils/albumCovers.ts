/**
 * Gets the official album cover url for any SEVENTEEN album
 */
export function getAlbumCoverUrl(title: string): string {
  if (!title) return TEAM_PROMOTIONAL_IMAGES[0];
  const cleanTitle = title.trim();

  // Try loading from localStorage custom overrides first
  if (typeof window !== 'undefined') {
    try {
      const saved = localStorage.getItem('custom_album_covers');
      if (saved) {
        const customMap = JSON.parse(saved);
        if (customMap[cleanTitle]) {
          return customMap[cleanTitle];
        }
        // Look for substring match in customMap
        for (const key of Object.keys(customMap)) {
          if (cleanTitle.toLowerCase().includes(key.toLowerCase()) || key.toLowerCase().includes(cleanTitle.toLowerCase())) {
            if (customMap[key]) return customMap[key];
          }
        }
      }
    } catch (e) {
      console.warn('Failed to parse custom_album_covers from localStorage', e);
    }
  }
  
  if (ALBUM_COVER_MAP[cleanTitle]) {
    return ALBUM_COVER_MAP[cleanTitle];
  }

  // Look for sub-string matches
  for (const key of Object.keys(ALBUM_COVER_MAP)) {
    if (cleanTitle.toLowerCase().includes(key.toLowerCase()) || key.toLowerCase().includes(cleanTitle.toLowerCase())) {
      return ALBUM_COVER_MAP[key];
    }
  }

  // Consistent fallback hash mapping to promotional photos
  let hash = 0;
  for (let i = 0; i < cleanTitle.length; i++) {
    hash = cleanTitle.charCodeAt(i) + ((hash << 5) - hash);
  }
  return TEAM_PROMOTIONAL_IMAGES[Math.abs(hash) % TEAM_PROMOTIONAL_IMAGES.length];
}

