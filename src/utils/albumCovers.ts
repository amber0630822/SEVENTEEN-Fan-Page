/**
 * Gets the official variety show cover url
 */
export function getVarietyCoverUrl(title: string): string {
  if (!title) return VARIETY_PROMOTIONAL_IMAGES[0];
  const cleanTitle = title.trim();

  // Try loading from localStorage custom overrides first
  if (typeof window !== 'undefined') {
    try {
      const saved = localStorage.getItem('custom_variety_covers');
      if (saved) {
        const customMap = JSON.parse(saved);
        if (customMap[cleanTitle]) {
          return customMap[cleanTitle];
        }
        const sortedCustomKeys = Object.keys(customMap).sort((a, b) => b.length - a.length);
        for (const key of sortedCustomKeys) {
          if (cleanTitle.toLowerCase().includes(key.toLowerCase()) || key.toLowerCase().includes(cleanTitle.toLowerCase())) {
            if (customMap[key]) return customMap[key];
          }
        }
      }
    } catch (e) {
      console.warn('Failed to parse custom_variety_covers from localStorage', e);
    }
  }
  
  const sortedKeys = Object.keys(VARIETY_COVER_MAP).sort((a, b) => b.length - a.length);
  for (const key of sortedKeys) {
    if (cleanTitle.toLowerCase().includes(key.toLowerCase())) {
      return VARIETY_COVER_MAP[key];
    }
  }

  let hash = 0;
  for (let i = 0; i < cleanTitle.length; i++) {
    hash = cleanTitle.charCodeAt(i) + ((hash << 5) - hash);
  }
  return VARIETY_PROMOTIONAL_IMAGES[Math.abs(hash) % VARIETY_PROMOTIONAL_IMAGES.length];
}

